void delay_us(uint16_t us) {
    __HAL_TIM_SET_COUNTER(&htim2, 0);
    while (__HAL_TIM_GET_COUNTER(&htim2) < us);
}

uint32_t readUltrasonicDistance(void) {
    uint32_t start = 0, stop = 0, duration = 0, timeout = 0;

    trig_low(); delay_us(2);
    trig_high(); delay_us(10);
    trig_low();

    while (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_RESET) {
        if (++timeout > TIMEOUT_US) return 0xFFFFFFFF;
    }
    start = __HAL_TIM_GET_COUNTER(&htim2);

    timeout = 0;
    while (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_SET) {
        if (++timeout > TIMEOUT_US) return 0xFFFFFFFE;
    }
    stop = __HAL_TIM_GET_COUNTER(&htim2);

    duration = (stop >= start) ? (stop - start) : (0xFFFF - start + stop);
    return (duration * 343) / 20000;
}

int __io_putchar(int ch){
	HAL_UART_Transmit(&huart2, (uint8_t *)&ch,1,HAL_MAX_DELAY);
	return ch;
}
